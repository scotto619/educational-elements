// Helper function to get progressive graph content based on week
export const getProgressiveGraphs = (weekNumber) => {
  const weekNum = parseInt(weekNumber.replace('week', ''));
  const content = literacyWarmupContent[weekNumber];
  
  if (!content) return [];
  
  let allGraphs = [];
  
  // Always include basic graphs (letters)
  allGraphs = [...content.graphReview.graphs];
  
  // Add digraphs progressively 
  allGraphs = [...allGraphs, ...content.graphReview.digraphs];
  
  // Add trigraphs from week 6 onwards
  if (weekNum >= 6) {
    allGraphs = [...allGraphs, ...content.graphReview.trigraphs];
  }
  
  // Add quadgraphs from week 10 onwards  
  if (weekNum >= 10) {
    allGraphs = [...allGraphs, ...content.graphReview.quadgraphs];
  }
  
  return allGraphs;
};